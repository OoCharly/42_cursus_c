#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*s;
	size_t	i;

	i = 0;
	if (!(s = (char*)ft_memalloc(sizeof(char) * (size + 1))))
		return (NULL);
	return (s);
}

#include "libft.h"

int		ft_strnoccur(char *s, int c, size_t n)
{
	int		out;
	size_t	i;

	out = 0;
	i = 0;
	while (*(s + i) && i < n)
	{
		if (*(s + i) == (char)c)
			out++;
		i++;
	}
	return (out);
}
